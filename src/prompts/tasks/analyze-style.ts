export function buildAnalyzePrompt(posts: string[]): string {
  return `
ЗАДАЧА: Проаналізувати стиль автора на основі наданих постів.

ПОСТИ ДЛЯ АНАЛІЗУ:
${posts.map((p, i) => `--- Пост ${i + 1} ---\n${p}`).join("\n\n")}

Проаналізуй і визнач:

1. TONE: Як би ти описав тон в 3-5 словах? Будь конкретним.
   НЕ "professional and engaging"
   А "впевнений, трохи іронічний, з повагою до читача"

2. FORMALITY: Де на шкалі від "месенджер з другом" до "Harvard Business Review"?

3. PERSONALITY: Який характер проступає через тексти? 2-3 речення.

4. SENTENCE STYLE: Типова довжина? Ритм? Фрагменти чи повні речення?

5. PARAGRAPH STYLE: Щільні чи повітряні? Скільки речень в абзаці?

6. HOOKS: Як автор починає пости? Виділи 3-4 патерни.
   Впорядкуй від найчастішого.

7. CLOSINGS: Як автор закінчує? 2-3 патерни.

8. SIGNATURE PHRASES: Які фрази повторюються? Характерні конструкції?
   Тільки реальні, не придумані.

9. AVOID: Чого автор НІКОЛИ не робить? Що відсутнє в текстах?

10. EMOJI: Як використовує? Часто / рідко / ніколи? Які саме?

Відповідай конкретно і з прикладами з постів.
Не узагальнюй. Не прикрашай. Описуй те що бачиш.
`;
}